echo "Enter the ID range of LXC containers that should be stopped:"
echo
read -p "$(echo "ID range from:   ")" FROM
read -p "$(echo "ID range to:     ")" TO   


#############################################################################
# START NODES
#############################################################################

for ((i=FROM; i<=TO; i++))
    do
        echo "Starting LXC container $i"
        pct start $i &
    done
	
sleep 10

echo
echo "Done!"
echo
echo

